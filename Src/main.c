void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	static uint8_t i = 0;

	if(huart->Instance == USART1)
	{
		if(UART1.rxbyte != 0xEF)
		{
			UART1.rxbuffer[i] = UART1.rxbyte;
			i++;
		}
		else
		{
			strcpy(UART1.buffercopy, UART1.rxbuffer);
			memset(UART1.rxbuffer, '\0', sizeof(UART1.rxbuffer));
			UART1.dataready = TRUE;
		}
	  HAL_UART_Receive_IT(&huart1, (uint8_t*) &UART1.rxbyte, 1);
	}
}

void CompareUARTdata()
 {
	UART1.dataready = FALSE;
	memset(usb.buffer, '\0', sizeof(usb.buffer));
 }

void CompareUSBdata()
 {
	//HAL_UART_Transmit(&huart1,&usb.buffer[0],strlen(usb.buffer),100);
	if(strncmp (usb.buffer,"play",4) == 0)
	{
		MP3_play();
	}
	if(strncmp (usb.buffer,"pause", 5) == 0)
	{
		MP3_pause();
	}
	else if(strncmp (usb.buffer,"init", 4) == 0)
	{
		MP3_Init();
	}
	else if(strncmp (usb.buffer,"start", 5) == 0)
	{
		MP3_playWithIndex();
	}
	memset(usb.buffer, '\0', sizeof(usb.buffer));
 }
